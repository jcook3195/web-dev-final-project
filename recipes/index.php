							<div class="two-thirds">
								<h2 class="recipe-title">GREEK MEATBALL PITA</h2>
								<div>
									<ul>
										<h3>TZATZIKI SAUCE:</h3>
										<li>1/2 cup grated seedless, English cucumber</li>
										<li>1 cup plain Greek yogurt</li>
										<li>1 clove garlic, finely minced</li>
										<li>1/4 teaspoon salt</li>
										<li>Pinch black pepper</li>
										<li>1 to 2 teaspoons fresh lemon juice</li>
										
										<h3>MEATBALLS:</h3>
										<li>1/2 cup bread crumbs</li>
										<li>1/3 cup milk</li>
										<li>1/4 cup chopped fresh parsley</li>
										<li>1 egg, lightly beaten</li>
										<li>1 tablespoon dried oregano</li>
										<li>2 cloves garlic, finely minced</li>
										<li>1 teaspoon salt</li>
										<li>1/2 teaspoon black pepper</li>
										<li>1 1/2 pounds ground turkey</li>
										
										<h3>PITAS + TOPPINGS:</h3>
										<li>4 whole wheat pita breads</li>
										<li>2 large tomatoes, sliced</li>
										<li>2 cups fresh spinach, arugula or a combination</li>
									</ul>
								</div>
								<p>For the tzatziki, place the grated cucumber in the center of a clean kitchen towel.
									Roll up the towel and squeeze out the excess water from the cucumber. Place the 
									cucumber in a medium bowl. Add the yogurt, garlic, salt, pepper and lemon juice.
									Stir to combine. Cover and refrigerate until ready to serve. This can be made several
									days in advance.
									For the meatballs, preheat the oven to 400°F. Line a baking sheet with foil and lightly
									grease the foil with cooking spray.
									In a medium bowl, stir together the bread crumbs and milk and let the mixture sit for a 
									few minutes to let the crumbs soften. Add the parsley, egg, oregano, garlic, salt, and 
									pepper. Stir to combine. Add the turkey and mix until well combined.
									Portion the meatballs into tablespoon-sized balls and place ½-inch apart on the prepared 
									baking sheet.
									Bake for 15 minutes until the meatballs are no longer pink in the center.
									To serve, slice the pita breads in half. Dollop a spoonful of tzatziki sauce on the bottom
									of each pita. Add a layer of tomatoes and greens. Stuff meatballs inside and serve.</p>

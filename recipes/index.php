								<h2 class="recipe-title">SMOKED TURKEY CLUB PANINI</h2>
								<div>
									<ul>
										<li>8-12 slices bacon</li>
										<li>1/3 cup sun-dried tomatoes, packed in oil, rinsed, patted dry and minced</li>
										<li>1/3 cup light mayonnaise</li>
										<li>8 slices thick-cut crusty bread</li>
										<li>1/2 pound thinly sliced deli Swiss cheese</li>
										<li>1/2 pound thinly sliced deli smoked turkey</li>
										<li>3 cups baby spinach or arugula</li>
									</ul>
								</div>
								<p>Cook bacon until crisp. You can use a skillet or use the microwave, placing the bacon
									in a single layer on a paper-towel lined plate, cover with paper towels and microwave
									for about 5 minutes (repeating the process if all the bacon doesn’t fit the first round).
									In a small bowl, combine the sun-dried tomatoes and mayonnaise.
									Lightly spread butter over one side of each bread. Lay the butter-sides down on a cutting
									board or work area. Spread each slice with the sun-dried tomato mayonnaise. Layer half of
									the cheese on 4 slices of bread then top with turkey, bacon, spinach (or arugula) and 
									remaining cheese. Top with remaining bread, butter side up.
									Cook the sandwiches on a panini press, skillet or grill pan heated on medium heat for one
									minute prior to cooking. I use an indoor grill pan for my paninis but a skillet would 
									work just as well. I place 2-3 sandwiches (depending on the size of the sandwich) in the
									grill pan and weigh the sandwiches down with a heavy pot (you can also use a foil-covered
									brick or similar object). Cook the sandwiches for about 4-5 minutes per side, until they 
									golden brown and the cheese is melted. Repeat with remaining sandwiches. Serve immediately.</p>
